package object;

public class Apply {
	
	private int postId;
	
	private int tutorId;
	
	
	

	public Apply(int postId, int tutorId) {
		super();
		this.postId = postId;
		this.tutorId = tutorId;
	}




	public int getPostId() {
		return postId;
	}




	public void setPostId(int postId) {
		this.postId = postId;
	}




	public int getTutorId() {
		return tutorId;
	}




	public void setTutorId(int tutorId) {
		this.tutorId = tutorId;
	}
	
	
	
	
	
	

}
